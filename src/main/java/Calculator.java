public class Calculator {


    public int calculatorAdd(int a, int b) {
       int c = a + b;
        return c;
    }

    public int calculatorSub(int a, int b){
        int c = a - b;
        return c;
    }

    public int calculatorMultiply(int a, int b){
        int c = a * b;
        return c;
    }
}


